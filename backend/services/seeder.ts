async function seed(): Promise<void> {
  await mongoose.connect(process.env.MONGODB_URI!);
  console.log('✅  MongoDB connected');

  const docs = RAW_DRAWS.map(([concurso, dateStr, numbers]) => {
    const { category, min, max } = classify(numbers);
    return {
      concurso: parseInt(concurso),
      date: new Date(dateStr + 'T12:00:00Z'),
      numbers,
      category,
      min,
      max,
    };
  });

  const ops = docs.map((d) => ({
    updateOne: {
      filter: { concurso: d.concurso },
      update: { $set: d },
      upsert: true,
    },
  }));

  const result = await Draw.bulkWrite(ops);
  console.log(`✅  Seeded: ${result.upsertedCount} inserted, ${result.modifiedCount} updated`);
  await mongoose.disconnect();
}

seed().catch((err: Error) => {
  console.error('❌ Seed error:', err.message);
  process.exit(1);
});
